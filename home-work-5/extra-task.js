function isUpperCase(string) {
  return string === string.toUpperCase();
}

console.log(isUpperCase('c'));
console.log(isUpperCase('C'));
console.log(isUpperCase('hello I AM DONALD'));
console.log(isUpperCase('HELLO I AM DONALD'));
console.log(isUpperCase('QWERTyUIOP'));
console.log(isUpperCase('QWERTYUIOP'));
